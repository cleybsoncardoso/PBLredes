    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nomeArquivo = null;
        while (nomeArquivo == null || nomeArquivo.equals("")) {
            nomeArquivo = JOptionPane.showInputDialog("Qual o nome do Arquivo que deseja criar?");
            if (nomeArquivo == null || nomeArquivo.equals("")) {
                JOptionPane.showMessageDialog(null, "Você informou o nome do Arquivo que deseja criar");
            }
        }
        if (nomeArquivo != null) {
            this.atualizarLista();
            if (!nomeArquivo.contains(".")) {
                nomeArquivo += ".txt";
            }
            if (!nomeArquivo.contains(".txt")) {
                JOptionPane.showMessageDialog(null, "Extensão inexistente");
                this.jButton1ActionPerformed(evt);
            } else if (arquivos.contains(nomeArquivo)) {
                JOptionPane.showMessageDialog(null, "Arquivo " + nomeArquivo + " já existente, coloque outro nome");
                this.jButton1ActionPerformed(evt);
            } else {
                controller.criarArquivo(nomeArquivo);
                JOptionPane.showMessageDialog(null, "Arquivo " + nomeArquivo + " Criado");
            }
        }
        this.atualizarLista();
    }//GEN-LAST:event_jButton1ActionPerformed

