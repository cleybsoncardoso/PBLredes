    private void atualizarLista() {
        arquivos = controller.listarArquivosTxt();
        String[] nomes = new String[arquivos.size()];
        for (int i = 0; i < arquivos.size(); i++) {
            nomes[i] = arquivos.get(i);
        }
        jList1.setListData(nomes);
    }

