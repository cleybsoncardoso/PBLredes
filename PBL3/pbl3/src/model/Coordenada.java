package model;

/**
 *
 * @author cleyb
 */
class Coordenada {
    private int x, y;

    public Coordenada(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
    
}
